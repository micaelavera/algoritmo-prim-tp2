package inteligencia;

public class Coordenada {

	private double latitud;
	private double longitud;
	
	public Coordenada(){
		latitud=0.0;
		longitud=0.0;
	}
	public Coordenada(double lat,double lon){
		latitud=lat;
		longitud=lon;
	}

	public double getLatitud() {
		return latitud;
	}

	public double getLongitud() {
		return longitud;
	}

	
	
	
	
	
	
	
}
